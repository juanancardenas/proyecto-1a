module.exports = {

    /**
     * @example
     * n1 = 1, n2 = 4 -> Returns 5
     * @param {*} n1 First number of the add
     * @param {*} n2 Second number of the add
     */
    suma: function (n1, n2) {
        return n1 + n2;
    },

    /**
     * @example
     * n1 = 1, n2 = 4 -> Returns -3
     * @param {*} n1 First number of the substract
     * @param {*} n2 Second number of the substract
     */
    resta: function (n1, n2) {
        return n1 - n2;
    },

    multiplicacion: function (n1, n2) {
        return n1 * n2;
    },

    division: function (n1, n2) {
        return n1 / n2;
    },

}